async function main() {
  const slugs = Object.keys(PRESETS_BY_SLUG);
  const doctors = await prisma.doctor.findMany({
    where: { slug: { in: slugs } },
    select: { id: true, clinicId: true, slug: true },
  });

  console.log(`Found ${doctors.length} matching doctor row(s) across all clinics.`);

  let total = 0;
  for (const d of doctors) {
    const presets = PRESETS_BY_SLUG[d.slug];
    if (!presets) continue;

    await prisma.doctorPreset.deleteMany({
      where: { clinicId: d.clinicId, doctorId: d.id },
    });

    const created = await prisma.doctorPreset.createMany({
      data: presets.map((p, i) => ({
        clinicId: d.clinicId,
        doctorId: d.id,
        field: p.field,
        label: p.label,
        fieldValue: p.fieldValue ?? p.label,
        noteTemplate: p.noteTemplate,
        sortOrder: i,
      })),
    });
    total += created.count;
    console.log(`  ${d.slug} (${d.id}) — seeded ${created.count} presets`);
  }
  console.log(`Done. ${total} presets total.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
